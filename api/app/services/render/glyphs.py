def _transform_path(d: str, sx: float, sy: float, tx: float, ty: float) -> str:
    """Apply scale (sx, sy) then translate (tx, ty) to an SVG path d-string.

    Only handles M, L, Q, C, Z commands (what fonttools SVGPathPen emits).
    Font coords have Y-up; we flip Y via sy = -scale.
    """
    out: list[str] = []
    # Tokenise: split into commands + number sequences
    tokens = _re.findall(r'[MLQCZmlqcz]|[-+]?(?:\d+\.?\d*|\.\d+)', d)
    i = 0
    while i < len(tokens):
        tok = tokens[i]
        if tok in ('Z', 'z'):
            out.append('Z')
            i += 1
        elif tok == 'M':
            x = float(tokens[i + 1]) * sx + tx
            y = float(tokens[i + 2]) * sy + ty
            out.append(f'M{x:.1f},{y:.1f}')
            i += 3
        elif tok == 'L':
            x = float(tokens[i + 1]) * sx + tx
            y = float(tokens[i + 2]) * sy + ty
            out.append(f'L{x:.1f},{y:.1f}')
            i += 3
        elif tok == 'Q':
            x1 = float(tokens[i + 1]) * sx + tx
            y1 = float(tokens[i + 2]) * sy + ty
            x = float(tokens[i + 3]) * sx + tx
            y = float(tokens[i + 4]) * sy + ty
            out.append(f'Q{x1:.1f},{y1:.1f} {x:.1f},{y:.1f}')
            i += 5
        elif tok == 'C':
            x1 = float(tokens[i + 1]) * sx + tx
            y1 = float(tokens[i + 2]) * sy + ty
            x2 = float(tokens[i + 3]) * sx + tx
            y2 = float(tokens[i + 4]) * sy + ty
            x = float(tokens[i + 5]) * sx + tx
            y = float(tokens[i + 6]) * sy + ty
            out.append(f'C{x1:.1f},{y1:.1f} {x2:.1f},{y2:.1f} {x:.1f},{y:.1f}')
            i += 7
        else:
            # Bare number — skip (shouldn't happen with well-formed d)
            i += 1
    return ''.join(out)


def text_to_path_d(
    text: str,
    x: float,
    y: float,
    font_size: float,
    anchor: str = "start",
    rotation_deg: float = 0,
    rotation_cx: float = 0,
    rotation_cy: float = 0,
) -> str:
    """Convert a text string to an SVG path `d` attribute.

    Parameters
    ----------
    text : str
        The text to render.
    x, y : float
        Anchor position in SVG coordinates (Y-down).
    font_size : float
        Desired font size in SVG units.
    anchor : str
        ``"start"`` (left-aligned), ``"middle"`` (centred), or ``"end"``.
    rotation_deg : float
        Optional rotation (degrees) applied around (rotation_cx, rotation_cy).

    Returns
    -------
    str
        Combined SVG path d-string for all glyphs (multiple sub-paths).
    """
    scale = font_size / UNITS_PER_EM
    # Font Y is up; SVG Y is down → flip via negative sy.
    sy = -scale
    sx = scale

    # Compute total advance for anchoring
    total_advance = sum(GLYPH_ADVANCES.get(ch, 0) for ch in text) * sx
    if anchor == "middle":
        x -= total_advance / 2
    elif anchor == "end":
        x -= total_advance

    # Baseline: font ascent is above baseline.  y in SVG = baseline.
    # In font coords, glyphs sit from DESCENT (negative) to ASCENT (positive).
    # After flipping Y, ASCENT maps to negative direction.
    # Place baseline so the visual centre of caps roughly aligns with y.
    baseline_y = y + ASCENT * scale * 0.35  # approx vertical-centre

    parts: list[str] = []
    cur_x = x
    rot_rad = rotation_deg * _math.pi / 180 if rotation_deg else 0

    for ch in text:
        glyph_d = GLYPH_PATHS.get(ch)
        adv = GLYPH_ADVANCES.get(ch, GLYPH_ADVANCES.get('0', 500))
        if glyph_d:
            transformed = _transform_path(glyph_d, sx, sy, cur_x, baseline_y)
            if rotation_deg:
                transformed = _rotate_path(transformed, rot_rad, rotation_cx, rotation_cy)
            parts.append(transformed)
        cur_x += adv * sx

    return ''.join(parts)


def _rotate_path(d: str, rad: float, cx: float, cy: float) -> str:
    """Rotate all coordinates in a path d-string around (cx, cy)."""
    cos_r = _math.cos(rad)
    sin_r = _math.sin(rad)

    def rot(x: float, y: float) -> tuple[float, float]:
        dx, dy = x - cx, y - cy
        return cx + dx * cos_r - dy * sin_r, cy + dx * sin_r + dy * cos_r

    out: list[str] = []
    tokens = _re.findall(r'[MLQCZmlqcz]|[-+]?(?:\d+\.?\d*|\.\d+)', d)
    i = 0
    while i < len(tokens):
        tok = tokens[i]
        if tok in ('Z', 'z'):
            out.append('Z')
            i += 1
        elif tok == 'M':
            rx, ry = rot(float(tokens[i + 1]), float(tokens[i + 2]))
            out.append(f'M{rx:.1f},{ry:.1f}')
            i += 3
        elif tok == 'L':
            rx, ry = rot(float(tokens[i + 1]), float(tokens[i + 2]))
            out.append(f'L{rx:.1f},{ry:.1f}')
            i += 3
        elif tok == 'Q':
            x1, y1 = rot(float(tokens[i + 1]), float(tokens[i + 2]))
            x2, y2 = rot(float(tokens[i + 3]), float(tokens[i + 4]))
            out.append(f'Q{x1:.1f},{y1:.1f} {x2:.1f},{y2:.1f}')
            i += 5
        elif tok == 'C':
            x1, y1 = rot(float(tokens[i + 1]), float(tokens[i + 2]))
            x2, y2 = rot(float(tokens[i + 3]), float(tokens[i + 4]))
            x3, y3 = rot(float(tokens[i + 5]), float(tokens[i + 6]))
            out.append(f'C{x1:.1f},{y1:.1f} {x2:.1f},{y2:.1f} {x3:.1f},{y3:.1f}')
            i += 7
        else:
            i += 1
    return ''.join(out)


def text_width(text: str, font_size: float) -> float:
    """Return the rendered width of *text* at *font_size*."""
    scale = font_size / UNITS_PER_EM
    return sum(GLYPH_ADVANCES.get(ch, 0) for ch in text) * scale

